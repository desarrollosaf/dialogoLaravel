                <p class="text-wrapper-12">Mujeres a través del tiempo en el Congreso mexiquense</p>
                <p class="text-wrapper-13">
                    Fue Clara del Moral de Lara del Distrito II de Lerma la primera mujer en ocupar una curul en la XXXIX
                    Legislatura mexiquense, que operó del 5 de septiembre de 1954 al 4 septiembre de 1957. Del Moral fue
                    presidenta de la Mesa Directiva, convirtiéndose así en la primera fémina en representar al Poder
                    Legislativo en la entidad y a su vez en incursionar en un ámbito que hasta ese momento era dominado por el
                    género masculino.
                </p>
            </div>
            <img class="clara-del-moral" src="{{asset('assets/paridad/img/clara-del-moral-acuarela-1.png')}}" />
            <div class="frame-19">
                <p class="text-wrapper-4">
                    Este hecho histórico ocurrió poco después de que en 1953 las mujeres obtuvieran el derecho al voto en el
                    país, así como la posibilidad de ser electas para cargos públicos. <br /><br />Clara Del Moral legisló a
                    favor de la educación, la salud y el bienestar de las comunidades vulnerables y sin duda, tiene un lugar
                    destacado en la historia del Estado de México, pues su incursión parlamentaria fue parteaguas de la
                    representación femenina en la política y a su vez, de la participación de las mujeres en la toma de
                    decisiones y el desarrollo de políticas públicas de la entidad.<br /><br />Sin embargo, para las mujeres
                    ocupar cargos de representación popular o directivos en la administración pública ha sido un proceso a
                    través del tiempo con diversos desafíos; pese a que en 1996, el entonces Instituto Federal Electoral
                    (IFE), ahora, Instituto Nacional Electoral (INE) fijó un límite del 70 por ciento de congresistas de un
                    mismo género, no fue, sino hasta 2007 que se estableció que, para integrar el Poder Legislativo se debía
                    cumplir con una cuota mínima de un 40 por ciento de personas de un mismo género.
                </p>
            </div>
            <div class="frame-19">
                <p class="text-wrapper-4">
                    A partir de la XXXIX Legislatura se fueron sumando poco a poco más mujeres a la historia del trabajo
                    legislativo en la entidad. En la XLIX Legislatura destaca la diputada Cecilia López Rodríguez del Distrito
                    V de Tenango del Valle quien fungió como miembro de la entonces Gran Comisión, convirtiéndose en la primer
                    diputada en formar parte de este órgano legislativo que más adelante se transformaría en la Junta de
                    Coordinación Política (Jucopo), la cual se integra por las personas que coordinan cada grupo
                    parlamentario. <br /><br />En la siguiente tabla se observa un listado histórico de las diputadas que han
                    sido integrantes de la Gran Comisión y/o Jucopo, resaltando que la priista Martha Hilda González Calderón
                    y la panista Selma Noemí Montenegro Andrade, fueron presidentas.
                </p>
