                        <div class="frame-9">
                            <p class="text-wrapper-3">
                                En principio, es pertinente aclarar que el Poder Judicial es el encargado de administrar la
                                justicia, mediante la aplicación e interpretación de las leyes en la resolución de conflictos, por
                                lo que estas reformas pretenden, en esencia, que esta función básica para cualquier sociedad
                                armónica sea más cercana, eficiente y transparente.<br />La parte fundamental de la reforma es la
                                democratización de este poder público mediante el voto popular. El Judicial, al igual que el
                                Legislativo y el Ejecutivo, serán conducidos por personas electas por sufragio universal, libre,
                                secreto y directo.<br /><br />La primera elección se realizará el próximo 1 de junio del 2025,
                                cuando se elegirá la totalidad de las y los integrantes del Tribunal de Disciplina Judicial, así
                                como la mitad de los cargos de magistraturas, mientras que la elección de personas juzgadoras solo
                                será para vacancias, retiros programados por jubilación, prejubilaciones o renuncias voluntarias,
                                pero la renovación de la totalidad de cargos deberá concluir en la elección ordinaria del 2027,
                                cuando se votará, de manera coincidente, por presidencias municipales, así como diputaciones locales
                                y federales. Ambos comicios serán organizados por el Instituto Electoral del Estado de México
                                (IEEM).
                            </p>
                            <img class="billete" src="https://c.animaapp.com/gsJY2lm3/img/billete-2-1@2x.png" />
                            <p class="text-wrapper-3">
                                Sin embargo, este proceso tendrá sus propias características. A diferencia de las personas
                                candidatas a cargos de elección popular que son postuladas por los partidos políticos, quienes
                                aspiren a integrar el Poder Judicial serán presentadas por los tres poderes (Ejecutivo, Legislativo
                                y Judicial) mediante su respectivo comité de evaluación, cuya función será identificar a los mejores
                                perfiles, una vez agotada la fase de participación pública, abierta, transparente, inclusiva y
                                accesible de aspirantes que cumplan los requisitos. En la mitad de las magistraturas a elegir se
                                incluyen 28 vacantes, de las cuales 21 obedecen a renuncias voluntarias, una por retiro programado y
                                seis por nueva creación. Cinco magistraturas adicionales corresponden al Tribunal de Disciplina
                                Judicial. En cuanto a la elección de personas juzgadoras, son 55 los espacios que estarán en disputa
                                debido a que quienes cumplían esta función no concluyeron los procesos de ratificación o los
                                respectivos concursos de oposición, o bien se jubilaron. A estos cargos se suma la elección de la
                                presidencia del Tribunal Superior de Justicia para sumar un total de 89 puestos en disputa.
                            </p>
