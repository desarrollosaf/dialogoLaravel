                    <div class="text-wrapper-4">Reestructuración orgánica</div>
                    <div class="frame-12">
                        <p class="text-wrapper-3">
                            Se crea el Tribunal de Disciplina Judicial y el Órgano de Administración Judicial, en sustitución del
                            actual Consejo de la Judicatura. El primero se define como el órgano con facultades para recibir
                            denuncias, investigar y sancionar a las personas servidoras públicas judiciales que no cumplan con sus
                            deberes de manera ética y legal, el cual tendrá una integración colegiada, conformada por cinco
                            personas electas por la&nbsp;&nbsp;iudadanía (el 1 de junio del 2025), y cuyo encargo durará seis
                            años, siendo sustituidas de manera escalonada, y sin poder ser electas para un nuevo periodo.
                        </p>
                        <p class="text-wrapper-3">
                            En cuanto al Órgano de Administración Judicial, cuyo pleno se conformará por cinco personas con un
                            mandato de seis años (una designada por Ejecutivo, otra por el Legislativo y tres más por el Tribunal
                            Superior de Justicia), podrá tomar decisiones relacionadas con la administración, manejo presupuestal
                            y carrera judicial; su presidencia será rotativa y durará dos años.
                        </p>
                    </div>
                </div>
            </div>
            <div class="frame-13">
                <div class="frame-14">
                    <div class="frame-15">
                        <div class="text-wrapper-5">Austeridad</div>
                        <p class="text-wrapper-6">
                            Uno de los principales reclamos populares es el dispendio de recursos públicos, por lo que el Poder
                            Judicial no podrá tener fondos, fideicomisos, mandatos o contratos análogos que no estén previstos en
                            la ley, además de que se elimina el haber de retiro (monto equivalente al 100% del sueldo neto el
                            primer año y del 80% los siguientes cinco años para las personas magistadas que culminaban su
                            periodo), además de que ninguna persona servidora pública podrá ganar más que la Presidenta de la
                            República.
                        </p>
                    </div>
                    <div class="frame-15">
                        <div class="text-wrapper-5">Supremacía constitucional</div>
                        <p class="text-wrapper-6">
                            Con las reformas, también se modifica el régimen de suspensiones en casos de controversias
                            constitucionales o acciones de inconstitucionalidad locales, para que no pueda otorgarse la<br />suspensión
                            de normas generales en dichos procesos, así como evitar la impugnación de alguna reforma o adición a
                            la Constitución.
                        </p>
