                            <p class="text-wrapper-3">
                                Este esfuerzo de las, le y los diputados de la LXII Legislatura mexiquense -cuya Junta de
                                Coordinación Política es presidida por el diputado José Francisco Vázquez Rodríguez (morena)-
                                destina recursos para avanzar en la construcción de una sociedad más justa,
                            </p>
                            <p class="text-wrapper-3">
                                equitativa e inclusiva, respetuosa de los derechos de todas las personas, que promueva el
                                empoderamiento de las mujeres, prevenga las violencias en su contra y luche por eliminar cualquier
                                tipo de discriminación en lo público y en lo privado.
                            </p>
