                    <p class="text-wrapper-7">
                        A la Fiscalía General de Justicia del Estado de México se le otorgó un presupuesto de 90 millones de
                        pesos para la atención que brinda este organismo a los delitos de violencia de género, asignados de la
                        siguiente manera:
                    </p>
                    <img class="presupuestomujeres" src="{{asset('assets/mujeresp/img/presupuestomujeres02-1-1.png')}}" />
                </div>
                <div class="frame-13">
                    <p class="text-wrapper-6">Secretaría de Seguridad del Estado de México</p>
                    <p class="text-wrapper-7">
                        Para la Secretaría de Seguridad del Estado de México se destinaron 5 millones de pesos para apoyar a
                        niñas, niños y adolescentes que viven con sus madres en los Centros de Reinserción Social, monto que
                        considera la entrega de canastas alimentarias y ropa. También, fueron presupuestados 400 mil pesos
                        destinados a espacios que atiendan las necesidades de mujeres embarazadas dentro de cada uno de estos
                        Centros.
                    </p>
                    <div class="frame-14">
                        <div class="text-wrapper-6">Salud</div>
                        <p class="text-wrapper-7">
                            En lo que refiere a los Servicios de Salud para la Mujer, la LXII Legislatura mexiquense aprobó 2 mil
                            691 millones 707 mil 353 pesos; mientras que, para las niñas, niños y adolescentes otorgó 4 mil 037
                            millones 502 mil 944 pesos. Además el Plan de Desarrollo Estatal 2023-2029 pretende que los programas
                            de salud para este género emigren de un modelo de atención a uno de prevención.
                        </p>
                    </div>
                    <div class="frame-14">
                        <div class="text-wrapper-6">Educación</div>
                        <p class="text-wrapper-7">
                            El presupuesto 2025 abonará a Programas y Proyectos Presupuestarios orientados a promover la educación
                            en temas de igualdad de género; el acceso de las, les y los estudiantes a una educación inclusiva y
                            equitativa; así como en la adopción de prácticas sostenibles que contribuyan a formar una sociedad
                            comprometida con el bienestar del planeta y toda la población.<br /><br />En ese sentido, destacan 20
                            millones de pesos que fueron asignados a la Secretaría de Educación, Ciencia, Tecnología e Innovación
                            con el propósito de entregar productos de gestión menstrual a las alumnas de escuelas públicas de
                            educación básica y media superior.
                        </p>
                    </div>
                    <div class="frame-15">
                        <div class="text-wrapper-6">Población LGBTTTIQ+</div>
                        <p class="text-wrapper-7">
                            Las, le y los diputados mexiquenses aprobaron por primera vez y de manera histórica un presupuesto de
                            36 millones 055 mil 097 pesos, para la población de la diversidad sexual, a través del proyecto
                            “Cultura de igualdad entre la mujer, el hombre y la población LGBTTTIQ+ para la prevención de la
                            violencia de género”, cuyo propósito es impulsar valores como el reconocimiento, la convivencia y la
                            no discriminación entre los diversos grupos poblacionales.<br /><br />Con lo anterior, queda asentado
                            que la LXII Legislatura mexiquense reafirma el compromiso con la igualdad de género y las mujeres, a
                            la vez que garantiza un presupuesto que atiende sus necesidades y promueve una sociedad inclusiva que
                            se abre cada vez más a la participación del género femenino desde los diferentes espacios de poder y
                            toma de decisiones.<br /><br />Nota: Si te interesa indagar más en el tema del Presupuesto 2025
