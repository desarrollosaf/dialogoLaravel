                <div class="frame-12">
                    <div class="text-wrapper-6">Secretaría de las Mujeres</div>
                    <p class="text-wrapper-7">
                        Para la Secretaría de las Mujeres -órgano gubernamental responsable de diseñar, coordinar e implementar
                        acciones, programas y políticas públicas que garanticen los derechos de las mujeres- la LXII Legislatura
                        mexiquense presupuestó 828 millones 598 mil 431 pesos.<br />En ese sentido, esta institución, además de
                        promover la igualdad de género, deberá atender la prevención y atención de la violencia hacia las
                        mujeres y las Declaratorias de Alerta de Violencia de Género relacionadas con feminicidios y
                        desapariciones en el Estado de México. Igualmente, deberá fomentar acciones que garanticen su
                        empoderamiento económico e impulsen su participación en la política y la sociedad.
                    </p>
                    <img class="presupuestomujeres" src="{{asset('assets/mujeresp/img/presupuestomujeres02-1.png')}}" />
