                <h2 class="h2-centrado">Son derechos de las personas usuarias de los servicios de salud:</h2>
            </div>
        </div>
        <div class="div-contenedor">
            <div class="div-block-5">
                <p class="texto-general">I. Recibir un trato digno, respetuoso, profesional y con enfoque humanizado.<br><br>II. Ser atendidas en todo momento con respeto a sus derechos fundamentales, su dignidad, privacidad, cultura y valores personales.<br><br>III. Ser informadas de manera clara, suﬁciente y precisa sobre su padecimiento, los tratamientos indicados y las implicaciones.<br><br>IV. Obtener servicios médicos adecuados, oportunos, eﬁcaces, de calidad y seguros.<br><br>V. No ser objeto de ningún tipo de discriminación.<br><br>VI. Recibir tratamiento médico acorde con principios cientíﬁcos reconocidos y aceptados.<br><br>VII. Recibir la prescripción de tratamientos en términos comprensibles y legibles, identiﬁcando los medicamentos de forma genérica.<br><br>VIII. Decidir libremente sobre su atención.<br><br>IX. Recibir atención médica en caso de urgencia.<br><br>X. Contar con alternativas de tratamiento y expresar su consentimiento informado.<br><br>XI. Que le sea garantizada una atención apegada a la conﬁdencialidad y la protección de la información relacionada con su estado de salud.<br><br>XII. Presentar quejas, inconformidades o sugerencias y recibir respuesta.<br><br>XIII. Rehusarse a participar en actividades de investigación o enseñanza médica.<br><br>XIV. Otorgar su consentimiento informado por escrito o de manera electrónica, mismo que será incorporado a su expediente clínico.<br><br>XV. Solicitar y recibir una segunda opinión médica sobre su diagnóstico o tratamiento.<br><br>XVI. Contar con una historia clínica que cumpla con las disposiciones de las normas oﬁciales mexicanas.<br><br>XVII. Recibir de manera alternativa la prestación de servicios médicos a distancia, que faciliten su atención sin necesidad de su desplazamiento, contemplados por la salud digital.<br><br>XVIII. Contar con un expediente clínico, preferentemente digital, al cual puedan acceder.<br><br>XIX. Solicitar la emisión de certiﬁcados relacionados con su estado de salud.<br><br>XX. Obtener información clara y detallada sobre el diagnóstico, los tratamientos recibidos y las recomendaciones para su evolución, al ﬁnalizar su estancia en una institución de salud.<br><br>XXI. En el caso de niñas, niños y adolescentes, ser llevados por sus madres, padres o representantes legales a revisiones integrales para favorecer su sano crecimiento.<br><br>XXII. Acceder a una muerte digna, con respeto a su voluntad respecto a tratamientos y acciones que deseen recibir o rechazar, preservando su dignidad humana.<br><br>XXIII. Obtener rehabilitación orientada a facilitar su reintegración en los ámbitos familiar, laboral y comunitario.<br><br>XXIV. Recibir los medicamentos necesarios, conforme al catálogo autorizado de medicamentos e insumos, y a que se le brinden alternativas sobre los centros donde haya existencia.</p>
