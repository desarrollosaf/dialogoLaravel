        <p class="body-text-general"><strong class="body-text-general">Dependiendo del país o región, se le llama parlamento, congreso, cámara, asamblea, corte o dieta, por mencionar algunos. La investigadora de la Universidad Nacional Autónoma de México (UNAM), Cecilia Mora-Donatto, experta en derecho constitucional y parlamentario, se refiere al Parlamento como un “órgano constitucional que ostenta la máxima representación popular y el generador de las normas con rango de ley, además de ser el controlador y fiscalizador de la acción de gobierno”.</strong> </p>
