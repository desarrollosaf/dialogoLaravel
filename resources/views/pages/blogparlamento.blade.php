        <h1 class="titulos-blog-parlamento">¿QUÉ ES UN PARLAMENTO?</h1>
        <p class="body-text-general">Para iniciar, es importante conocer el significado de Parlamento.<br>Para la Real Academia de la Lengua Española (RAE), el Parlamento es una “Cámara o asamblea legislativa, nacional o regional”.<br><br>Por su parte, el Diccionario Universal de Términos Parlamentarios [1] señala que parlamento deriva del latín <em>parabolare</em> y del francés <em>parlament</em> que significan hablar.<br><br>Con este nombre se designa al órgano que representa al Poder Legislativo de un Estado.</p>
