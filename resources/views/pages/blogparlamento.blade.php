            <h1 class="titulos-blog-parlamento">Características de la LXII Legislatura mexiquense</h1>
            <div>
                <div class="columns-10 w-row">
                    <div class="w-col w-col-6">
                        <h3 class="titulos-blog-parlamento">Incluyente y paritaria</h3>
                        <p class="body-text-general">La LXII Legislatura del Estado de México tiene una serie de cualidades que le dan una dimensión histórica. Es la primera en la historia, por ejemplo, con una integración auténticamente paritaria, toda vez que se integra por 37 diputadas, 37 diputados y une diputade. Adicionalmente, por acciones afirmativas, cuenta con representantes de los pueblos y comunidades indígenas, afrodescendientes y población LGBTTTIQ+ (lesbianas, gais, bisexuales, transgénero, travestis, transexuales, intersexuales, <em>queer</em> y más).</p>
                    </div>
                    <div class="w-col w-col-6">
                        <h3 class="titulos-blog-parlamento">De diálogo y consenso</h3>
                        <p class="body-text-general">El consenso entre los grupos parlamentarios de morena, PVEM, PT, PRI, PAN, MC y PRD ha distinguido el procesamiento y aprobación del 95 % de las iniciativas por unanimidad, de acuerdo con el informe del diputado José Francisco Vázquez Rodríguez (morena), presidente de la Junta de Coordinación Política (Jucopo), correspondiente al primer año de trabajo legislativo.</p>
                    </div>
                </div>
                <h3 class="titulos-blog-parlamento">Atenta y organizada</h3>
                <p class="body-text-general">Organizada en 38 comisiones permanentes, cinco comisiones especiales y cinco comités, la LXII Legislatura mexiquense ha priorizado un debate profundo sobre el alcance y las posibles repercusiones de reformas, leyes o acuerdos, conforme a criterios técnicos y con base en las necesidades prácticas de las y los mexiquenses; por lo tanto, ha contado con la participación de personas servidoras públicas, activistas y ciudadanía involucrada durante las reuniones deliberativas de estos órganos parlamentarios.</p>
