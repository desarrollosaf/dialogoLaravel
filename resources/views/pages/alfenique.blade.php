    <section>
        <h1 class="titulo-alfe-ique space-btwn">Datos curiosos acerca del Dulce de Alfeñique:</h1>
        <p class="texto-general"><strong>• El nombre alfeñique proviene del árabe al-fanid, que significa “pasta de azúcar estirada”.<br>• Hay familias toluqueñas que
                llevan más de cinco generaciones dedicadas a esta dulce tradición. <br>• En sus orígenes, se elaboraba con miel de abeja, antes de que el azúcar la reemplazara.
                <br>• Las figuras de este dulce tienen significados simbólicos: las calaveras representan la vida después de la muerte, los ataúdes nos recuerdan con humor y ternura
                a quienes ya partieron y los corazones simbolizan el amor eterno.</strong></p>
    </section>
