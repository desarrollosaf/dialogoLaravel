        <h1 class="titulo-alfe-ique space-btwn">¿Cómo surge el alfeñique en Toluca?</h1>
        <div>
            <div class="w-row">
                <div class="w-col w-col-6">
                    <p class="texto-general">La cultura en México es basta y diversa, sus tradiciones y costumbres reflejan la historia y la identidad de cada
                        región, permitiendo comprender nuestro pasado, valorar nuestro presente y proyectar el futuro; en ese sentido, los mexiquenses gozan
                        de un patrimonio cultural tangible e intangible de gran riqueza, reconocido a nivel nacional e internacional, como sus zonas arqueológicas,
                        museos, obras de arte, costumbres, celebraciones y diversas formas de expresión.<br>‍<br>Entre estas manifestaciones destaca el Alfeñique
                        -tradición centenaria y símbolo de identidad cultural de Toluca-, que ha trascendido generaciones y constituye un elemento distintivo de la
                        festividad del Día de Muertos, no solo en el municipio, sino también en el ámbito nacional e internacional.</p>
                </div>
                <div class="w-col w-col-6">
                    <p class="texto-general">El Alfeñique llegó a México en la época colonial, fusionándose con tradiciones prehispánicas hasta convertirse
                        en el dulce típico que conocemos en la actualidad. En 1630, don Francisco de la Rosa obtuvo autorización para elaborar alfeñique en
                        Toluca, lo que marcó el inicio de esta tradición regional.<br><br>La Feria del Alfeñique se celebra en Toluca desde 1932, en los Portales
                        del centro histórico (Reforma, Madero y 20 de Noviembre), del mes de octubre a inicios de noviembre; de acuerdo con el cronista Francisco
                        Estrada, los primeros alfeñiques representaban borreguitos como símbolo de gratitud religiosa, evolucionando posteriormente hacia una mayor
                        variedad de figuras.</p>
                </div>
            </div>
