                        personas en<br />situación de discapacidad, adultas mayores, viudas, madres o padres solteros sin
                        ingresos fijos, al igual que para personas físicas cuya percepción diaria no rebase dos salarios mínimos
                        generales vigentes y personas liberadas con motivo de amnistía estatal.<br /><br />En cuanto a la
                        población en general, se conserva el bono en el pago del 8, 6 y 4% del predial durante enero, febrero y
                        marzo, respectivamente, al igual que el estímulo por cumplimiento puntual en el pago de los dos últimos
                        años, en 8%, 6% y 2% como bonificación adicional, aplicable en enero, febrero y marzo,
                        respectivamente.<br /><br />Sobre el agua, conserva la bonificación de 8%, 6% y 4% durante enero,
