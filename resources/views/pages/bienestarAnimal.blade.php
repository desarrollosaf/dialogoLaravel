                    <p class="texto-cuerpo">Contexto…<br><br>Durante años, cientos de animales en la entidad han vivido entre el abandono, el maltrato, la indiferencia y la violencia normalizada. Desde perros encadenados a la intemperie toda su vida, gatos envenenados para no molestar a los vecinos, caballos y burros sobreexplotados y golpeados “como castigo” por no seguir de pie con sus pesadas cargas, y un sin fin de casos crueles, donde los agresores no recibían consecuencias reales por el maltrato que ejercían.</p>
