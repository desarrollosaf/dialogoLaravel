        <p class="texto-cuerpo">De acuerdo con datos del Consejo Ciudadano siete de cada diez animales domésticos sufren algún tipo de crueldad lo que provoca la muerte de alrededor de 60 mil animales al año en el país. Además, penosamente México ocupa el tercer lugar mundial y el primero en Latinoamérica en casos de maltrato animal.<br><br>Como parteaguas para el surgimiento de esta Ley, se encuentra el caso que se hizo público el 28 de febrero de 2025, cuando la Fiscalía General del Estado de México llevó a cabo el rescate de 108 canes de diversas razas y tamaños, los cuales se encontraban encadenados en condiciones insalubres y de hacinamiento dentro de un rastro clandestino en el municipio de Mexicaltzingo.</p>
