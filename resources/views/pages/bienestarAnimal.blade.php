    <div class="seccion">
        <h1 class="h3-centrado">¿Cuáles son las obligaciones que adquirimos las personas ante los animales respecto a esta Ley?</h1>
        <div class="div-block-4">
            <div class="w-row">
                <div class="w-col w-col-6">
                    <p class="texto-cuerpo">•Proteger a los animales, garantizar su bienestar, brindarles atención, asistencia, auxilio y buen trato; así como velar por su desarrollo natural, salud y evitarles el maltrato, la crueldad, el sufrimiento y la zoofilia.<br><br>•Denunciar, ante las autoridades correspondientes (Procuraduría de Protección al Ambiente del Estado de México PROPAEM, instituciones de Seguridad o ante el Juez Conciliador de cada municipio), cualquier irregularidad o violación a esta Ley, cometidas por cualquier particular, profesionista, asociaciones protectoras o autoridades.</p>
                </div>
                <div class="w-col w-col-6">
                    <p class="texto-cuerpo">•Inscribir a sus mascotas en el Padrón de Animales Domésticos del Estado de México.<br><br>•Promover en todas las instancias públicas o privadas, la cultura de protección, atención y buen trato hacia los animales.<br><br>•Participar en instancias de carácter social que cuiden, asistan y protejan a los animales.</p>
