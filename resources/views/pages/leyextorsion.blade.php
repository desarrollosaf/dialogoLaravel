        <h2 class="headin2">En resumen, lo que debes saber:</h2>
        <p class="texto-general">La extorsión no desaparece del marco jurídico. Lo que ocurre es que se deroga su regulación en el Código Penal estatal para armonizarla con la Ley General federal, que es donde se establecía el tipo penal, las sanciones y las agravantes; con ello se fortalece su combate, se persigue de oficio y se atiende con instituciones especializadas.</p>
