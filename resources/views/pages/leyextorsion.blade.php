        <h2 class="headin2">¿El cobro de piso está contemplado<br>para ser sancionado?</h2>
        <p class="texto-general">Sí, el cobro de piso se combate como una forma de extorsión y se persigue de oficio, conforme a la Ley General y la nueva ley estatal.</p>
        <h2 class="headin2">¿La extorsión desde los penales se perseguirá?</h2>
        <p class="texto-general">Sí, los penales tienen150 días hábiles, desde que entró en vigor la normatividad, para implementar tecnología que inhiba llamadas y comunicaciones usadas para extorsionar.</p>
        <h2 class="headin2">¿Qué cambia para las víctimas?</h2>
        <p class="texto-general">• Ya no es indispensable que ratifiquen la denuncia.<br>• Habrá atención especializada.<br>• Se reduce el riesgo de represalias.</p>
        <h2 class="headin2">¿Qué nuevas instituciones se crean?</h2>
        <p class="texto-general">• Fiscalía Central Especializada<br>• Centro Estatal de Atención a Denuncias<br>• Consejo Estatal de Prevención y Combate a la Extorsión</p>
        <h2 class="headin2">¿Qué se aprobó?</h2>
        <p class="texto-general">• Nueva Ley estatal contra la extorsión<br>• Reformas al Código Penal y a la Ley de la Fiscalía</p>
