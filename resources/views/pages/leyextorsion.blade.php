        <h2 class="titulo_franja">¿Por qué se creó una nueva ley?</h2>
    </div>
    <div class="div-section-2">
        <p class="texto-general">El 6 de julio de 2025 se presentó la Estrategia Nacional contra la Extorsión, derivado de ello, el 9 de octubre de 2025 se publicó la reforma constitucional para facultar al Congreso de la Unión a expedir una ley general a fin homologar el tipo penal de extorsión en todo el país y obligar a las entidades a armonizar su marco jurídico a ese nuevo modelo constitucional.</p>
        <h2 class="headin2">¿En qué consistió el proceso<br>para la creación de la nueva ley?</h2>
        <p class="texto-general">1. Se derogó el delito de extorsión del Código Penal del Estado de México.<br><br>2. Se expidió una ley estatal que regula de manera Integral la prevención, persecución y sanción del delito de extorsión, fortaleciendo las acciones institucionales para su erradicación.</p>
        <h2 class="headin2">¿La extorsión deja de ser un delito<br>o se deja de sancionar?</h2>
        <p class="texto-general">No, sigue siendo delito, pero se sancionará conforme a la Ley General, es decir, federal, en su Artículo 15 que señala penas de 15 a 25 años de prisión y una multa de 300 a 500 veces el valor diario de la Unidad de Medida y Actualización y con agravantes en distintas modalidades.</p>
