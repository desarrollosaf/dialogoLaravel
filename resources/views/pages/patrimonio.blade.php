        <div class="frame-20">
            <div class="div">
                <p class="text-wrapper">Factores que determinan el valor de una zona</p>
                <div class="frame-21">
                    <p class="text-wrapper-10">
                        Por ejemplo, si en el 2023 a un lado de una casa habitación había un basurero, pero un año después se
                        construyó, en ese mismo sitio, un centro comercial, vialidades y un proyecto inmobiliario, el costo del
                        terreno y la casa será más alto, por lo que las tablas de valores registrarán esa variación y el
                        ayuntamiento correspondiente, si lo considera pertinente, podrá cobrar un predial más alto debido a la
                        plusvalía. Por el contrario, si un desastre natural afecta una región sería injusto que las personas
                        propietarias de un negocio siguieran pagando lo mismo cuando el valor y la actividad comercial disminuyó
                        considerablemente.
                    </p>
                    <p class="text-wrapper-10">
                        Existe la percepción equivocada de que estas tablas, que proponen los municipios y valida o rechaza la
                        Legislatura, determinan directamente el impuesto predial. Se trata de una imprecisión porque, si bien
                        son una de las variables que toman en cuenta los ayuntamientos para calcular este impuesto, en la
                        práctica la relación no es equivalente, ya que la autonomía municipal, basada en el federalismo fiscal,
                        les da un margen más amplio de decisión, además de que el predial se basa en el valor comercial del
                        inmueble o terreno, no solo el catastral que resulta ser más bajo.<br /><br />Para el 2025, la mayoría
                        de los ayuntamientos hicieron ajustes al alza, pero en un rango que no supera el 10%.
                    </p>
