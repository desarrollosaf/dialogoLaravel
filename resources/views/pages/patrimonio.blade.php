                    <p class="text-wrapper-9">
                        Son los propios ayuntamientos los que mandan al IGECEM sus propuestas con un soporte documental, es
                        decir operaciones inmobiliarias y comerciales, así como comparativos con zonas similares para justificar
                        actualizaciones. Este organismo de la Secretaría de Finanzas del Estado de México hace una revisión
                        técnica y manda su opinión a los municipios para que hagan los ajustes pertinentes y validen sus tablas
                        en sesión de cabildo.<br /><br />Para verificar que haya una justificación adecuada, o bien se
                        rectifiquen los números y criterios, hay un último filtro: el Poder Legislativo del Estado de México.
                    </p>
                    <p class="text-wrapper-9">
                        Por eso, las, le y los diputados cotejan los datos, aclaran sus dudas con el IGECEM y aprueben o
                        rechazan incrementos o ratificaciones, primero en comisiones y, posteriormente, en el Pleno para que
                        estas tablas sean aplicadas a partir del 1 de enero de cada año. Este ajuste permite que se reconozca
                        oficialmente el valor de los predios, de acuerdo a las condiciones de las inmediaciones de cada terreno
                        o inmueble.
                    </p>
                </div>
