                        <p class="parrafo-blanco-general-izquierda">
                            • Se preparan las piezas decorativas, como conos, tiras de papel “chinito” y flores.
                            <br><br>
                            • Se ensamblan las diferentes partes del cuerpo de la piñata con engrudo.
                            <br><br>
                            • Se pueden emplear técnicas específicas para darle forma a la decoración, como la de &quot;churrito&quot; enrollando papel o la &quot;bombita&quot; doblando el papel para crear un efecto abombado.
                            <br><br>
                            • Se utiliza papel crepé de colores que es más flexible que el papel de china.
                            <br><br>
                            • Para las piñatas de barro, se usa la técnica &quot; &quot;colita de rata&quot; o &quot;popotillo&quot; para rellenar los huecos.
